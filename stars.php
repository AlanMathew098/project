        <h1 style="font-weight: 600;">Cleaning and sorting</h1>
        <p>Most fine coffee goes through a battery of machines that sort the coffee by the density of bean and by bean size, all the while removing sticks, rocks, nails, and miscellaneous debris that may have become mixed with the coffee during drying. First machines blow the beans into the air; those that fall into bins closest to the air source are heaviest and biggest; the lightest (and likely defective) beans plus chaff are blown in the farthest bin. Other machines shake the beans through a series of sieves, sorting them by size. Finally, a machine called a gravity separator shakes the sized beans on a tilted table, so that the heaviest, densest and best vibrate to one side of the pulsating table, and the lightest to the other.
         
