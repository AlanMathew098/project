        <h1 style="font-weight: 600;">Decaffeination</h1>
        <p>Decaffeination is the process of extracting caffeine from green coffee beans prior to roasting. The most common decaffeination process used in the United States is supercritical carbon dioxide (CO2) extraction. In this process, moistened green coffee beans are contacted with large quantities of supercritical CO2 (CO2 maintained at a pressure of about 4,000 pounds force per square inch (28 MPa) and temperatures between 90 and 100 °C (194 and 212 °F)), which removes about 97% of the caffeine from the beans. The caffeine is then recovered from the CO2, typically using an activated carbon adsorption system.

          Another commonly used method is solvent extraction, typically using oil (extracted from roasted coffee) or ethyl acetate as a solvent. In this process, solvent is added to moistened green coffee beans to extract most of the caffeine from the beans. After the beans are removed from the solvent, they are steam-stripped to remove any residual solvent. The caffeine is then recovered from the solvent, and the solvent is re-used. </p>
